/** Ищет координаты по точному названию города (без учёта регистра/пробелов). */
export function lookupCityCoordinates(city: string | null | undefined): [number, number] | null {
  const trimmed = city?.trim();
  if (!trimmed) return null;
  if (CITY_COORDINATES[trimmed]) return CITY_COORDINATES[trimmed];
  const lower = trimmed.toLowerCase();
  const key = Object.keys(CITY_COORDINATES).find((k) => k.toLowerCase() === lower);
  return key ? CITY_COORDINATES[key] : null;
}
