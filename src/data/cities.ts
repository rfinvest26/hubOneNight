const ALL_CITIES: string[] = Array.from(
  new Set(Object.values(CITIES_BY_COUNTRY).flat())
).sort((a, b) => a.localeCompare(b, 'ru'));

export function getCitySuggestions(query: string, country?: string | null): string[] {
  const pool = country && CITIES_BY_COUNTRY[country]
    ? CITIES_BY_COUNTRY[country]
    : ALL_CITIES;

  if (!query || query.trim() === '') {
    return pool.slice(0, 8);
  }

  const lower = query.toLowerCase();

  return pool
    .filter((c) => c.toLowerCase().includes(lower))
    .sort((a, b) => {
      const al = a.toLowerCase();
      const bl = b.toLowerCase();
      const aS = al.startsWith(lower);
      const bS = bl.startsWith(lower);
      if (aS && !bS) return -1;
      if (!aS && bS) return 1;
      return a.localeCompare(b, 'ru');
    })
    .slice(0, 8);
}
